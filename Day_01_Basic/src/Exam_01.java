import java.lang.*;

public class Exam_01 // 파일명과 똑같
{
	public static void main(String[] ar) //초기 실행 위치 
	{
		System.out.println("Hello World");
		System.out.print("Java ");
		System.out.println("Practice");
		
		//escape sequence 라고 부름, 즉 탈출문자
		// 제외하고 싶은 문자가 있다면 앞에 역슬래시(\)를 붙여주면 된다.
		// ex) ("\"Hello world\"")
		
		//code assistant -> syso
		
		// () <-- 소괄호, {} <--중괄호, [] <-- 대괄호 
		
		/*	문자열을 출력할 때는 " 를 사용한다.
		 *  문자를 출력할 때는 ' 를 사용한다.
		 * 	숫자를 출력할 때는 따옴표를 사용하지 않는다. 
		 */
		
		//convention - 관습, 관례
		/* BSD Style
		 * public class Exam_01
		 * {
		 * public static void main(String[] args)
		 * 	{
		 * 
		 * 	}
		 * }
		 * */
		
		/* GNU Style
		 * public class Exam_01
		 * 	{
		 * public static void main(String[] args)
		 * 		{
		 * 
		 * 		}
		 * 	}
		 * */
		
		/* K&R Style
		 * public class Exam_01 {
		 * public static void main(String[] args) {
		 * 
		 * 	}
		 * }
		 * */
		 
	}
}
