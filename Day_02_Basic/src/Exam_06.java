
public class Exam_06 {
	public static void main(String[] args) {
		
		// 변수 - 변할 수 있는 수 
		
		int a = 10;
		
		// 상수 - 항상 같은 수
		//		상징적인 특정 값을 사용하기 편하게 이름으로 고정시키는 용도로 많이 사용함.
		//		변수 앞에 final 키워드를 붙여서 사용하며, final 키워드가 붙은 변수는
		//		값을 변경할 수 없음.
		
		final int b = 10; 
		
		// 이름을 다 대문자로 씀, 에러가 나는 것은 아니나 관
		final double PI = 3.14;
		
	}

}
